package week1.day2;

public class LearnForLoop {
	
	
	public static void main(String[] args) {
		
		//Syntax : for loop
			// for (intialization; loop condition; increment/decrement condition)
		
		for(int i=1;i<=100; i++) {
			System.out.println("Hello "+i);
		}
	
		
		// Print numbers from 1 to 10
			// shortcut to create a for loop
				// type for and press a key ctrl + spacebar and press down arrow and press enter
		for (int i = 1; i <=10; i++) {
			System.out.println(i);
		}
		
		
	}

}
